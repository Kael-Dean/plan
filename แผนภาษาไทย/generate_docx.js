const {
  Document, Packer, Paragraph, TextRun, AlignmentType,
  TabStopType, TabStopPosition, PageBreak, LevelFormat,
  HeadingLevel, BorderStyle
} = require('docx');
const fs = require('fs');

// ============================================================
// Style constants — Thai government document standard
// ============================================================
const FONT = "TH SarabunPSK";
const SIZE_NORMAL = 32;   // 16pt (docx uses half-points)
const SIZE_HEADER = 32;   // 16pt
const SIZE_TITLE = 36;    // 18pt for plan title

// ============================================================
// Helper functions
// ============================================================
const T = (text, opts = {}) => new TextRun({
  text,
  font: FONT,
  size: SIZE_NORMAL,
  ...opts
});

const B = (text, opts = {}) => T(text, { bold: true, ...opts });

// Plain paragraph
const P = (children, opts = {}) => new Paragraph({
  children: Array.isArray(children) ? children : [children],
  ...opts
});

// Centered bold title paragraph
const titleP = (text) => new Paragraph({
  alignment: AlignmentType.CENTER,
  children: [new TextRun({ text, font: FONT, size: SIZE_TITLE, bold: true })],
  spacing: { after: 120 }
});

// Two-column header line using right-aligned tab stop
const headerLineP = (left, right) => new Paragraph({
  tabStops: [{ type: TabStopType.RIGHT, position: TabStopPosition.MAX }],
  children: [
    new TextRun({ text: left, font: FONT, size: SIZE_NORMAL, bold: true }),
    new TextRun({ text: "\t" + right, font: FONT, size: SIZE_NORMAL, bold: true })
  ]
});

// Separator dotted line
const separatorP = () => new Paragraph({
  alignment: AlignmentType.CENTER,
  children: [new TextRun({
    text: ".".repeat(110),
    font: FONT,
    size: SIZE_NORMAL
  })],
  spacing: { after: 120 }
});

// Bold heading (main section like "๑. สาระสำคัญ")
const mainHeadingP = (text) => new Paragraph({
  children: [new TextRun({ text, font: FONT, size: SIZE_NORMAL, bold: true })],
  spacing: { before: 120, after: 60 }
});

// Bold sub-heading (like "๑. ความคิดรวบยอด")
const subHeadingP = (text, indent = 720) => new Paragraph({
  indent: { left: indent },
  children: [new TextRun({ text, font: FONT, size: SIZE_NORMAL, bold: true })],
  spacing: { after: 60 }
});

// Numbered item (เลขไทย)
const numberedP = (numText, content, indent = 720) => new Paragraph({
  indent: { left: indent, hanging: 360 },
  children: [
    new TextRun({ text: `${numText}  `, font: FONT, size: SIZE_NORMAL }),
    new TextRun({ text: content, font: FONT, size: SIZE_NORMAL })
  ],
  spacing: { after: 40 }
});

// Bullet dash item
const dashP = (content, indent = 1080) => new Paragraph({
  indent: { left: indent, hanging: 240 },
  children: [
    new TextRun({ text: "-  ", font: FONT, size: SIZE_NORMAL }),
    new TextRun({ text: content, font: FONT, size: SIZE_NORMAL })
  ],
  spacing: { after: 40 }
});

// Standard text paragraph with indent
const indentP = (content, indent = 1080) => new Paragraph({
  indent: { left: indent },
  children: [new TextRun({ text: content, font: FONT, size: SIZE_NORMAL })],
  spacing: { after: 40 }
});

// Standard mark (มาตรฐาน ท x.x ...)
const standardP = (code, desc) => new Paragraph({
  indent: { left: 720, hanging: 360 },
  children: [
    new TextRun({ text: `มาตรฐาน ${code}  `, font: FONT, size: SIZE_NORMAL, bold: true }),
    new TextRun({ text: desc, font: FONT, size: SIZE_NORMAL })
  ],
  spacing: { after: 60 }
});

// Indicator paragraph (มาตรฐาน ท x.x ป.๒/x ...)
const indicatorP = (code, desc) => new Paragraph({
  indent: { left: 720 },
  children: [
    new TextRun({ text: `มาตรฐาน ${code}  `, font: FONT, size: SIZE_NORMAL, bold: true }),
    new TextRun({ text: desc, font: FONT, size: SIZE_NORMAL })
  ],
  spacing: { after: 40 }
});

