// Thai numerals
const THAI_NUMS = ["๑", "๒", "๓", "๔", "๕", "๖", "๗", "๘", "๙", "๑๐"];

// ============================================================
// Build a single plan
// ============================================================
function buildPlan(plan, isFirst) {
  const children = [];

  // Page break before each plan except the first
  if (!isFirst) {
    children.push(new Paragraph({ children: [new PageBreak()] }));
  }

  // Title
  children.push(titleP(`แผนการจัดการเรียนรู้ที่  ${plan.num}`));

  // Header lines
  children.push(headerLineP("กลุ่มสาระการเรียนรู้ภาษาไทย", "ชั้นประถมศึกษาปีที่  ๒"));
  children.push(headerLineP("หน่วยการเรียนรู้ที่ ๑ เรื่อง น้ำใส", "เวลา  ๑๕  ชั่วโมง"));
  children.push(headerLineP(`เรื่อง  ${plan.topic}`, `เวลา  ${plan.hours}  ชั่วโมง`));

  // Separator
  children.push(separatorP());

  // Standard area
  children.push(new Paragraph({
    children: [new TextRun({ text: plan.standard.saraName, font: FONT, size: SIZE_NORMAL, bold: true })],
    spacing: { after: 60 }
  }));
  children.push(standardP(plan.standard.code, plan.standard.desc));

  // Section 1: สาระสำคัญ
  children.push(mainHeadingP("๑.  สาระสำคัญ"));
  children.push(subHeadingP(`๑.  ${plan.keyConcept.title}`));
  children.push(new Paragraph({
    indent: { left: 1440 },
    children: [new TextRun({ text: plan.keyConcept.text, font: FONT, size: SIZE_NORMAL })],
    spacing: { after: 60 }
  }));
  children.push(subHeadingP("๒.  สมรรถนะสำคัญของผู้เรียน"));
  plan.competencies.forEach(c => children.push(dashP(c, 1440)));

  // Section 2: ตัวชี้วัด
  children.push(mainHeadingP("๒.  ตัวชี้วัด"));
  plan.indicators.forEach(i => {
    children.push(indicatorP(i.code, i.desc));
  });

  // Section 3: จุดประสงค์การเรียนรู้
  children.push(mainHeadingP("๓.  จุดประสงค์การเรียนรู้"));
  plan.objectives.forEach((o, idx) => {
    children.push(numberedP(`${THAI_NUMS[idx]}.`, o, 720));
  });

  // Section 4: คุณลักษณะอันพึงประสงค์
  children.push(mainHeadingP("๔.  คุณลักษณะอันพึงประสงค์"));
  DESIRABLE_TRAITS.forEach((t, idx) => {
    children.push(numberedP(`${THAI_NUMS[idx]}.`, t, 720));
  });

  // Section 5: สาระการเรียนรู้
  children.push(mainHeadingP("๕.  สาระการเรียนรู้"));
  plan.content.forEach(c => children.push(dashP(c, 720)));

  // Section 6: กิจกรรมการเรียนรู้
  children.push(mainHeadingP("๖.  กิจกรรมการเรียนรู้"));
  if (plan.activityNumbering === "manual") {
    // Plan 1 has the song in the middle, handle manually
    let count = 0;
    plan.activities.forEach(act => {
      if (typeof act === "string") {
        count++;
        children.push(numberedP(`${THAI_NUMS[count-1]}.`, act, 720));
      } else if (act.center && act.bold) {
        children.push(new Paragraph({
          alignment: AlignmentType.CENTER,
          children: [new TextRun({ text: act.text, font: FONT, size: SIZE_NORMAL, bold: true })],
          spacing: { after: 40 }
        }));
      } else if (act.center && act.noNumber) {
        children.push(new Paragraph({
          alignment: AlignmentType.CENTER,
          children: [new TextRun({ text: act.text, font: FONT, size: SIZE_NORMAL })],
          spacing: { after: 40 }
        }));
      } else if (act.num) {
        children.push(numberedP(act.num, act.text, 720));
        if (act.subs) act.subs.forEach(s => children.push(dashP(s, 1440)));
      } else if (act.text) {
        count++;
        children.push(numberedP(`${THAI_NUMS[count-1]}.`, act.text, 720));
        if (act.subs) act.subs.forEach(s => children.push(dashP(s, 1440)));
      }
    });
    // Override: Plan 1 first activity is the song intro, so adjust
  } else {
    plan.activities.forEach((act, idx) => {
      if (typeof act === "string") {
        children.push(numberedP(`${THAI_NUMS[idx]}.`, act, 720));
      } else {
        children.push(numberedP(`${THAI_NUMS[idx]}.`, act.text, 720));
        if (act.subs) act.subs.forEach(s => children.push(dashP(s, 1440)));
      }
    });
  }

  // Section 7: สื่อ / แหล่งการเรียนรู้
  children.push(mainHeadingP("๗.  สื่อ / แหล่งการเรียนรู้"));
  plan.materials.forEach((m, idx) => {
    children.push(numberedP(`${THAI_NUMS[idx]}.`, m, 720));
  });

  // Section 8: วัดผลประเมินผล
  children.push(mainHeadingP("๘.  วัดผลประเมินผล"));
  children.push(subHeadingP("๑.  วิธีการวัดและประเมินผล"));
  plan.methods.forEach(m => children.push(dashP(m, 1440)));
  children.push(subHeadingP("๒.  เครื่องมือการประเมิน"));
  plan.tools.forEach(t => children.push(dashP(t, 1440)));
  children.push(subHeadingP("๓.  เกณฑ์การประเมิน"));
  ASSESSMENT_CRITERIA.forEach(c => children.push(dashP(c, 1440)));

  return children;
}

// ============================================================
// Build the document
// ============================================================
const allChildren = [];
plans.forEach((plan, idx) => {
  const planChildren = buildPlan(plan, idx === 0);
  allChildren.push(...planChildren);
});

const doc = new Document({
  styles: {
    default: {
      document: {
        run: { font: FONT, size: SIZE_NORMAL }
      }
    }
  },
  sections: [{
    properties: {
      page: {
        size: { width: 11906, height: 16838 }, // A4
        margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 }
      }
    },
    children: allChildren
  }]
});

Packer.toBuffer(doc).then(buffer => {
  const outputPath = "d:\\แผน\\หน่วยการเรียนรู้ที่๑_น้ำใส.docx";
  fs.writeFileSync(outputPath, buffer);
  console.log("DOCX created successfully at:", outputPath);
  console.log("File size:", buffer.length, "bytes");
});
